clients = []
players = {}

def handle_client(conn, addr):
    try:
        print(f"[CONNECTED] {addr} connected.")
        conn.sendall("Welcome to Pokémon Battle!\nChoose your Pokémon:\n".encode())

        for name in pokemon_data:
            conn.sendall(f"- {name}\n".encode())

        choice = conn.recv(1024).decode().strip()

        if choice not in pokemon_data:
            conn.sendall("Invalid choice. Disconnecting.\n".encode())
            conn.close()
            return

        # Store player's data
        players[conn] = {
            "name": choice,
            "hp": pokemon_data[choice]["hp"],
            "attack": pokemon_data[choice]["attack"],
            "art": pokemon_data[choice]["art"]
        }

        # Send the Pokémon ASCII art
        conn.sendall(f"You selected {choice}!\n{pokemon_data[choice]['art']}\nWaiting for opponent...\n".encode())

        clients.append(conn)

        if len(clients) == 2:
            start_battle()

    except Exception as e:
        print(f"[ERROR] Connection lost from {addr}: {e}")
        conn.close()

def start_battle():
    p1, p2 = clients[0], clients[1]

    p1.sendall("Battle Start!\n".encode())
    p2.sendall("Battle Start!\n".encode())

    turn = 0

    while True:
        attacker = clients[turn % 2]
        defender = clients[(turn + 1) % 2]

        attacker_data = players[attacker]
        defender_data = players[defender]

        try:
            attacker.sendall("Your turn! Press Enter to attack.\n".encode())
            attacker.recv(1024)

            damage = attacker_data["attack"] + random.randint(-5, 5)
            defender_data["hp"] -= damage

            message = f"{attacker_data['name']} dealt {damage} damage! {defender_data['name']} HP: {defender_data['hp']}\n"
            attacker.sendall(message.encode())
            defender.sendall(message.encode())

            if defender_data["hp"] <= 0:
                attacker.sendall("You Win!\n".encode())
                defender.sendall("You Lose!\n".encode())
                break

            turn += 1

        except:
            print("[ERROR] Player disconnected.")
            break

    p1.close()
    p2.close()
    clients.clear()
    players.clear()

def start_server():
    server = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    server.bind((HOST, PORT))
    server.listen(2)
    print(f"[STARTED] Server running on port {PORT}")

    while True:
        conn, addr = server.accept()
        threading.Thread(target=handle_client, args=(conn, addr), daemon=True).start()

if __name__ == "__main__":
    start_server()